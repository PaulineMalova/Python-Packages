def add(a,b):
	c=a+b
	print(c)


def subtract(d,e):
	f=d-e
	print(f)


def multiply(a,b):
	c=a*b
	print(c)


def divide(d,e):
	f=d/e
	print(f)


def modulus(a,b):
	c=a%b
	print(c)


def whole_division(d,e):
	f=d//e
	print(f)

def percentage(a,b):
	c=(b/a)*100
	print("{}%".format(c))


def exponent(d,e):
	f=d**e
	print(f)


